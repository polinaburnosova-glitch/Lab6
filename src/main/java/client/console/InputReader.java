package client.console;

import common.model.HumanBeing;

/**
 * Интерфейс для чтения данных о человеке из источника ввода.
 *
 * <p>Предоставляет абстрактный метод для чтения всех необходимых
 * полей объекта HumanBeing. Конкретные реализации определяют,
 * откуда именно происходит чтение (консоль, файл, скрипт и т.д.).</p>
 *
 * <p>Данный интерфейс используется для обеспечения гибкости приложения:
 * можно легко заменить источник ввода без изменения основной логики.</p>
 *
 * @author Полина
 * @version 1.0
 * @since 2026-04-20
 * @see HumanBeing
 * @see client.console.ConsoleInputReader
 */
public interface InputReader {

    /**
     * Считывает и создает объект HumanBeing из источника ввода.
     *
     * <p>Метод должен последовательно запросить все поля, необходимые
     * для создания объекта HumanBeing, включая координаты и машину.
     * В случае неверного ввода должна выполняться повторная попытка
     * ввода до получения корректного значения.</p>
     *
     * @return полностью сконструированный объект HumanBeing
     * @throws RuntimeException если произошла ошибка при чтении данных
     *                          (например, достигнут конец файла в скрипте)
     */
    HumanBeing readHumanBeing();
}
