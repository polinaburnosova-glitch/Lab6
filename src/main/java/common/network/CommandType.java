package common.network;

import java.io.Serializable;

/**
 * Перечисление типов команд, поддерживаемых клиент-серверным приложением.
 *
 * <p>Каждая команда соответствует определенному действию над коллекцией
 * объектов HumanBeing. Команды могут выполняться как в интерактивном
 * режиме (с консоли), так и из файлов-скриптов.</p>
 *
 * <p>Перечисление реализует {@link Serializable} для возможности передачи
 * команд по сети между клиентом и сервером.</p>
 *
 * @author Полина
 * @version 1.0
 * @since 2026-04-20
 * @see common.network.Request
 * @see common.model.HumanBeing
 */
public enum CommandType implements Serializable {

    /**
     * Добавление нового элемента в коллекцию.
     * Требует объект HumanBeing в качестве аргумента.
     */
    ADD,

    /**
     * Обновление существующего элемента по ID.
     * Требует ID элемента и новый объект HumanBeing.
     */
    UPDATE,

    /**
     * Удаление элемента из коллекции по ID.
     * Требует числовой идентификатор элемента.
     */
    REMOVE_BY_ID,

    /**
     * Удаление первого элемента из коллекции.
     * Не требует аргументов.
     */
    REMOVE_FIRST,

    /**
     * Очистка всей коллекции.
     * Не требует аргументов.
     */
    CLEAR,

    /**
     * Вывод всех элементов коллекции в отсортированном порядке.
     * Не требует аргументов.
     */
    SHOW,

    /**
     * Вывод информации о коллекции (тип, дата создания, количество элементов).
     * Не требует аргументов.
     */
    INFO,

    /**
     * Вывод элемента с минимальным ID.
     * Не требует аргументов.
     */
    MIN_BY_ID,

    /**
     * Фильтрация элементов по настроению (mood).
     * Требует значение перечисления {@link common.model.Mood}.
     */
    FILTER_BY_MOOD,

    /**
     * Фильтрация элементов по префиксу названия саундтрека.
     * Требует строку-префикс для поиска.
     */
    FILTER_STARTS_WITH_SOUNDTRACK_NAME,

    /**
     * Добавление нового элемента, если его impactSpeed
     * больше максимального значения в коллекции.
     * Требует объект HumanBeing.
     */
    ADD_IF_MAX,

    /**
     * Добавление нового элемента, если его impactSpeed
     * меньше минимального значения в коллекции.
     * Требует объект HumanBeing.
     */
    ADD_IF_MIN,

    /**
     * Вывод справки по всем доступным командам.
     * Не требует аргументов.
     */
    HELP,

    /**
     * Завершение работы клиентского приложения.
     * Не требует аргументов.
     */
    EXIT,

    /**
     * Выполнение команд из файла-скрипта.
     * Требует путь к файлу в качестве аргумента.
     */
    EXECUTE_SCRIPT,
    LOGIN,
    REGISTER
}